function getProductAsHtmlString (prod)  {
  return `
  <article class="shoe one">
    <img src="${prod.img}" alt="Product Image" class="${prod.category}">
    <h2>${prod.name}</h2>
    <h3>$${prod.price}</h3>
    <h4>${prod.brand}</h4>
    <p>${prod.description}</p>
  </article>`;
}

function isMatchingName(product) {
  // Trim the input of white space, then uppercase it, then check if it's included
  if (product.name.toUpperCase().includes(this.trim().toUpperCase())) {
    return true;
  } else {
    return false;
  }

}


function renderProducts(arr) {
  document.getElementById('products').innerHTML = arr.map(getProductAsHtmlString).join('');
}

renderProducts(products);

function loadProductByName(products) {
  const nameImSearchingFor = event.target.value; 
  renderProducts(products.filter(isMatchingName, nameImSearchingFor));
}

document.getElementById('products').addEventListener('input', loadProductByName);